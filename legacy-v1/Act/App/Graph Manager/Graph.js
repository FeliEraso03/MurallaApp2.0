/**
 * Clase Graph que representa un grafo con nodos y aristas.
 */
export class Graph {
    /**
     * Constructor de la clase Graph.
     * Inicializa los arrays de nodes y edges.
     */
    constructor() {
        this.nodes = []; // Array para almacenar los nodos del grafo.
        this.edges = []; // Array para almacenar las aristas del grafo.
    }
}
